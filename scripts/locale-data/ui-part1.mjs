function ui({
  title,
  loading,
  wk,
  subtitle,
  updated,
  groupTitle,
  groupPhase,
  viewStandings,
  standingsTitle,
  standingsSubtitle,
  standingsGroup,
  backToPoster,
  position,
  team,
  played,
  won,
  drawn,
  lost,
  goalDiff,
  points,
  champion,
  championUnknown,
  trophyAlt,
  r32,
  r16,
  qf,
  sf,
  finals,
  thirdPlace,
  final,
  live,
  matchNumber,
  versus,
  timeSuffix,
  nowPlaying,
  viewAria,
  list,
  poster,
  zoomAria,
  zoomOut,
  zoomIn,
  fit,
  fitAria,
  loadFailed,
  unknown,
}) {
  return {
    app: { title, loading },
    header: { titleWk: wk, subtitle, lastUpdated: updated },
    group: { title: groupTitle, phase: groupPhase, viewStandings },
    standings: {
      title: standingsTitle,
      subtitle: standingsSubtitle,
      group: standingsGroup,
      backToPoster,
      position,
      team,
      played,
      won,
      drawn,
      lost,
      goalDiff,
      points,
    },
    champion: { label: champion, unknown: championUnknown, trophyAlt },
    rounds: { r32, r16, qf, sf, finals, thirdPlace, final },
    match: { live, number: matchNumber, versus, timeSuffix },
    live: { nowPlaying },
    view: { ariaLabel: viewAria, list, poster },
    zoom: { ariaLabel: zoomAria, zoomOut, zoomIn, fit, fitAria },
    errors: { loadFailed, unknown },
  }
}

